// Global variables
int ir[6];

bool hasArrived()
{
	const int arrivedThreshold = 105;
	for (int i = 1; i <= 5; i++)
	{
		if (ir[i] > arrivedThreshold) return true;
	}
	return false;
}

task main()
{

	const int maxSpeed = 60;

	bool wentForwardAlready = false;

	int lastLeft = 1;

	setLeft(maxSpeed);
	setRight(maxSpeed);
	wait1Msec(1500);
	setLeft(0);
	setRight(0);

	while(true)
	{

		if (!hasArrived())
		{

			HTIRS2readAllACStrength(irSeeker, ir[1], ir[2], ir[3], ir[4], ir[5]);

			int direction = HTIRS2readACDir(irSeeker);

			// These variables are used only once, because RobotC can't hang
			if (direction == 0)
			{
				if (!wentForwardAlready)
				{
					setLeft(maxSpeed);
					setRight(maxSpeed);
					wait1Msec(1000);
					wentForwardAlready = true;
				}
				else
				{
					setLeft(lastLeft * maxSpeed);
					setRight(-1 * lastLeft * maxSpeed);
				}
			}
			else
			{
				wentForwardAlready = false;

				if (direction < 5)
				{
					setLeft(maxSpeed * -1);
					setRight(maxSpeed * 1);
					lastLeft = -1;
				}
				else if (direction > 5)
				{
					setLeft(maxSpeed * 1);
					setRight(maxSpeed * -1);
					lastLeft = 1;
				}
				else if (direction == 5)
				{
					setLeft(maxSpeed);
					setRight(maxSpeed);
					clearTimer(T1);
					while (!hasArrived() && time1[T1] < 3000)	{}
				}
			}

			eraseDisplay();
			displayTextLine(0,"HT IR Seeker");
			displayTextLine(2, "dir: %3d", direction);
			displayTextLine(3, "ir1: %3d", ir[1]);
			displayTextLine(4, "ir2: %3d", ir[2]);
			displayTextLine(5, "ir3: %3d", ir[3]);
			displayTextLine(6, "ir4: %3d", ir[4]);
			displayTextLine(7, "ir5: %3d", ir[5]);

		}

		if (hasArrived())
		{
			setLeft(0);
			setRight(0);
			// Todo: Lift, dump, celebrate
			eraseDisplay();
			displayTextLine(3, "YOU HAVE ARRIVED");
			displayTextLine(4, "AT YOUR");
			displayTextLine(5, "DESTINATION");
			wait1Msec(100);
		}

	}

}
